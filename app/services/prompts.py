WRITE_CHAPTER_SYSTEM = """\
Ты — эксперт-аналитик и научный автор. Твоя задача — написать одну главу \
исследовательской работы на основе предоставленных материалов.

Правила форматирования:
- Всегда отвечай на языке исходного запроса.
- Используй только Markdown-разметку: заголовки (##, ###), абзацы и списки (- item).
- Первая строка ответа — заголовок главы (## ...).
- Не выходи за рамки текущей главы: не добавляй заголовки других глав.

Правила содержания:
- Опирайся на содержание предоставленных саммари источников.
- Не пересказывай источники дословно — анализируй и делай выводы.
- Учитывай уже написанные разделы, чтобы не повторяться.\
"""

WRITE_CHAPTER_USER = """\
## Исходный запрос
{query}

## Тематические ориентиры исследования
{direction}

## Полное содержание (оглавление) исследования
{structure}

## Саммари источников (JSON)
{summaries_json}

## Уже написанные разделы
{written_so_far}

## Задача
Напиши следующую главу исследования согласно содержанию:

{chapter}

Начни ответ с заголовка этой главы (##). Раскрой тему подробно, \
опираясь на саммари источников. Не повторяй то, что уже написано выше.\
"""


def build_write_chapter_messages(
    query: str,
    direction: str,
    summaries: list[dict],
    structure: str,
    chapter: str,
    written_so_far: str,
) -> list[dict]:
    """Формирует список сообщений для написания одной главы исследования.

    Args:
        query: Первичный исследовательский запрос пользователя.
        direction: Результат шага direction_brainstorm (векторы исследования).
        summaries: Список словарей с ключами 'url' и 'summary'.
        structure: Полное оглавление исследования (результат шага plan_structure).
        chapter: Текущая глава (заголовок h2 + подзаголовки h3).
        written_so_far: Ранее написанный текст (срез последних ~10к токенов).

    Returns:
        Список сообщений в формате OpenAI Chat для передачи в LLMClient.generate().
    """
    import json as _json

    summaries_json = _json.dumps(summaries, ensure_ascii=False, indent=2)
    user_content = WRITE_CHAPTER_USER.format(
        query=query,
        direction=direction,
        structure=structure,
        summaries_json=summaries_json,
        written_so_far=written_so_far or "(ещё ничего не написано)",
        chapter=chapter,
    )
    return [
        {"role": "system", "content": WRITE_CHAPTER_SYSTEM},
        {"role": "user", "content": user_content},
    ]


