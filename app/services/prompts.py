"""Промпты для LLM-шагов исследовательского пайплайна."""

from pydantic import BaseModel, Field

# ---------------------------------------------------------------------------
# Схемы структурированных ответов
# ---------------------------------------------------------------------------


class SearchResultToScore(BaseModel):
    """Входные данные для оценки одного результата поиска."""

    title: str
    description: str


class SearchResultScores(BaseModel):
    """10-балльная оценка одного результата поиска.

    Каждый балл — float от 0.0 до 1.0 с шагом 0.1:
      point_1  — Тематическая релевантность: насколько тема материала совпадает с запросом.
      point_2  — Соответствие направлениям: связь с векторами исследования.
      point_3  — Достоверность источника: авторитетность сайта/издания.
      point_4  — Глубина раскрытия: детальность и полнота материала.
      point_5  — Научная ценность: наличие фактов, данных, исследований.
      point_6  — Актуальность: свежесть и современность информации.
      point_7  — Уникальность: новизна информации относительно других результатов.
      point_8  — Практическая применимость: полезность для проведения исследования.
      point_9  — Качество описания: информативность заголовка и сниппета.
      point_10 — Общая ценность: итоговая оценка полезности для исследователя.
    """

    point_1: float
    point_2: float
    point_3: float
    point_4: float
    point_5: float
    point_6: float
    point_7: float
    point_8: float
    point_9: float
    point_10: float


# ---------------------------------------------------------------------------
# Шаг 1: направление исследования
# ---------------------------------------------------------------------------

DIRECTION_SYSTEM = """\
Ты — экспертный аналитик и методолог исследований. Твоя задача — определить \
ключевые направления для глубокого изучения темы: найти смежные подтемы, \
выявить неочевидные связи и сформулировать конкретные исследовательские векторы.

Правила:
- Всегда отвечай на языке исходного запроса.
- Каждое направление должно быть конкретным и операционализируемым — \
  его можно проверить через поиск или источники.
- Избегай банальных обобщений («изучить тему подробнее»).
- Структурируй ответ как пронумерованный список векторов исследования.\
"""

DIRECTION_USER = """\
## Запрос исследования
{query}

## Задача
Предложи {n_vectors} векторов для углублённого изучения этой темы.

Формат каждого пункта:
[Номер]. [Название вектора] — [1–2 предложения: суть направления и почему оно важно \
для понимания исходного запроса]
"""


def build_direction_messages(
    query: str,
    n_vectors: int = 5,
) -> list[dict]:
    """Формирует список сообщений для шага определения направления исследования.

    Args:
        query: Первичный исследовательский запрос пользователя.
        n_vectors: Количество векторов, которые должна предложить модель.

    Returns:
        Список сообщений в формате OpenAI Chat для передачи в LLMClient.generate().
    """
    user_content = DIRECTION_USER.format(query=query, n_vectors=n_vectors)
    return [
        {"role": "system", "content": DIRECTION_SYSTEM},
        {"role": "user", "content": user_content},
    ]


# ---------------------------------------------------------------------------
# Шаг 2: генерация поисковых запросов
# ---------------------------------------------------------------------------

SEARCH_KEYWORDS_SYSTEM = """\
Ты — экспертный аналитик поиска. Твоя задача — сформулировать точные и разнообразные \
поисковые запросы для поисковой системы на основе темы исследования и векторов его развития.

Правила:
- Всегда отвечай на языке исходного запроса.
- Каждый запрос должен быть самодостаточным — понятным без контекста.
- Запросы должны покрывать разные аспекты темы, избегая дублирования.
- Используй конкретные термины, имена, даты, если они уместны.
- Отвечай ТОЛЬКО валидным JSON-массивом строк, без пояснений и markdown-блоков.\
"""

SEARCH_KEYWORDS_USER = """\
## Тема исследования
{query}

## Векторы развития исследования
{direction}

## Задача
Сформулируй ровно {n_keywords} поисковых запросов для поисковой системы, \
которые помогут найти наиболее релевантную информацию по теме и её векторам.

Ответь строго в формате JSON-массива строк:
["запрос 1", "запрос 2", ...]
"""


def build_search_keywords_messages(
    query: str,
    direction: str,
    n_keywords: int = 5,
) -> list[dict]:
    """Формирует список сообщений для шага генерации поисковых запросов.

    Args:
        query: Первичный исследовательский запрос пользователя.
        direction: Результат шага direction_brainstorm (векторы исследования).
        n_keywords: Количество поисковых запросов для генерации.

    Returns:
        Список сообщений в формате OpenAI Chat для передачи в LLMClient.generate().
    """
    user_content = SEARCH_KEYWORDS_USER.format(
        query=query,
        direction=direction,
        n_keywords=n_keywords,
    )
    return [
        {"role": "system", "content": SEARCH_KEYWORDS_SYSTEM},
        {"role": "user", "content": user_content},
    ]


# ---------------------------------------------------------------------------
# Шаг 3а: оценка релевантности результатов поиска
# ---------------------------------------------------------------------------

RELEVANCE_SYSTEM = """\
Ты — эксперт по оценке качества информации. Твоя задача — оценить каждый результат \
веб-поиска по 10 критериям относительно темы и векторов исследования.

Критерии оценки (каждый от 0.0 до 1.0 с шагом 0.1):
  point_1  — Тематическая релевантность: насколько тема материала совпадает с запросом.
  point_2  — Соответствие направлениям: связь с векторами исследования.
  point_3  — Достоверность источника: авторитетность сайта/издания.
  point_4  — Глубина раскрытия: детальность и полнота материала.
  point_5  — Научная ценность: наличие фактов, данных, исследований.
  point_6  — Актуальность: свежесть и современность информации.
  point_7  — Уникальность: новизна информации относительно других результатов.
  point_8  — Практическая применимость: полезность для проведения исследования.
  point_9  — Качество описания: информативность заголовка и сниппета.
  point_10 — Общая ценность: итоговая оценка полезности для исследователя.

Правила:
- Отвечай ТОЛЬКО валидным JSON-массивом, без пояснений и markdown-блоков.
- Количество объектов в ответе должно строго совпадать с количеством входных результатов.
- Порядок объектов в ответе соответствует порядку входных результатов.\
"""

RELEVANCE_USER = """\
## Тема исследования
{query}

## Векторы исследования
{direction}

## Поисковый запрос
{keyword}

## Результаты для оценки (JSON-массив)
{results_json}

## Задача
Оцени каждый результат по 10 критериям.

Ответь строго в формате JSON-массива объектов:
[
  {{"point_1": 0.8, "point_2": 0.7, "point_3": 0.9, "point_4": 0.6, "point_5": 0.5, "point_6": 0.7, "point_7": 0.4, "point_8": 0.8, "point_9": 0.6, "point_10": 0.7}},
  ...
]
"""


def build_relevance_messages(
    query: str,
    direction: str,
    keyword: str,
    results: list[SearchResultToScore],
) -> list[dict]:
    """Формирует список сообщений для оценки релевантности результатов поиска.

    Args:
        query: Первичный исследовательский запрос пользователя.
        direction: Результат шага direction_brainstorm (векторы исследования).
        keyword: Поисковый запрос, по которому получены результаты.
        results: Список результатов для оценки.

    Returns:
        Список сообщений в формате OpenAI Chat для передачи в LLMClient.generate().
    """
    import json as _json

    results_json = _json.dumps(
        [{"title": r.title, "description": r.description} for r in results],
        ensure_ascii=False,
        indent=2,
    )
    user_content = RELEVANCE_USER.format(
        query=query,
        direction=direction,
        keyword=keyword,
        results_json=results_json,
    )
    return [
        {"role": "system", "content": RELEVANCE_SYSTEM},
        {"role": "user", "content": user_content},
    ]
