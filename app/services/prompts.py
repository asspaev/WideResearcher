SEARCH_KEYWORDS_SYSTEM = """\
Ты — экспертный аналитик поиска. Твоя задача — сформулировать точные и разнообразные \
поисковые запросы для поисковой системы на основе темы исследования и векторов его развития.

Правила:
- Всегда отвечай на языке исходного запроса.
- Каждый запрос должен быть самодостаточным — понятным без контекста.
- Запросы должны покрывать разные аспекты темы, избегая дублирования.
- Используй конкретные термины, имена, даты, если они уместны.
- Отвечай ТОЛЬКО валидным JSON-массивом строк, без пояснений и markdown-блоков.\
"""

SEARCH_KEYWORDS_USER = """\
## Тема исследования
{query}

## Векторы развития исследования
{direction}

## Задача
Сформулируй ровно {n_keywords} поисковых запросов для поисковой системы, \
которые помогут найти наиболее релевантную информацию по теме и её векторам.

Ответь строго в формате JSON-массива строк:
["запрос 1", "запрос 2", ...]
"""


def build_search_keywords_messages(
    query: str,
    direction: str,
    n_keywords: int = 5,
) -> list[dict]:
    """Формирует список сообщений для шага генерации поисковых запросов.

    Args:
        query: Первичный исследовательский запрос пользователя.
        direction: Результат шага direction_brainstorm (векторы исследования).
        n_keywords: Количество поисковых запросов для генерации.

    Returns:
        Список сообщений в формате OpenAI Chat для передачи в LLMClient.generate().
    """
    user_content = SEARCH_KEYWORDS_USER.format(
        query=query,
        direction=direction,
        n_keywords=n_keywords,
    )
    return [
        {"role": "system", "content": SEARCH_KEYWORDS_SYSTEM},
        {"role": "user", "content": user_content},
    ]


# ---------------------------------------------------------------------------
# Планирование структуры исследования
# ---------------------------------------------------------------------------

PLAN_STRUCTURE_SYSTEM = """\
Ты — архитектор исследований. Твоя задача — разработать чёткое, полное \
содержание (оглавление) для исследовательской работы на основе запроса, \
тематических ориентиров и собранных материалов.

Правила:
- Всегда отвечай на языке исходного запроса.
- Выводи ТОЛЬКО иерархическое оглавление в формате Markdown-заголовков: # / ## / ###.
- Никакого текста, пояснений, вступлений или заключений — только структура.
- Структура должна быть логичной и полной: на её основе можно написать \
  развёрнутое исследование.
- ЗАПРЕЩЕНО использовать слова «Вектор», «Направление», «Аспект» и порядковые \
  номера в заголовках.\
"""

PLAN_STRUCTURE_USER = """\
## Исходный запрос
{query}

## Тематические ориентиры исследования
{direction}

## Саммари источников (JSON)
{summaries_json}

## Задача
Разработай содержание (оглавление) исследовательской работы по данному запросу. \
Структура должна охватывать все ключевые аспекты темы и завершаться разделом выводов.

Формат вывода (пример):
# [Название всего исследования]
## [Название главы]
### [Название подглавы]
### [Название подглавы]
## [Название главы]
### [Название подглавы]
## Выводы
"""


def build_plan_structure_messages(
    query: str,
    direction: str,
    summaries: list[dict],
) -> list[dict]:
    """Формирует список сообщений для шага планирования структуры исследования.

    Args:
        query: Первичный исследовательский запрос пользователя.
        direction: Результат шага direction_brainstorm (векторы исследования).
        summaries: Список словарей с ключами 'url' и 'summary'.

    Returns:
        Список сообщений в формате OpenAI Chat для передачи в LLMClient.generate().
    """
    import json as _json

    summaries_json = _json.dumps(summaries, ensure_ascii=False, indent=2)
    user_content = PLAN_STRUCTURE_USER.format(
        query=query,
        direction=direction,
        summaries_json=summaries_json,
    )
    return [
        {"role": "system", "content": PLAN_STRUCTURE_SYSTEM},
        {"role": "user", "content": user_content},
    ]


