WRITE_ARTICLE_SYSTEM = """\
Ты — эксперт-аналитик и научный автор. Твоя задача — написать глубокую, \
структурированную исследовательскую статью на основе предоставленных материалов.

Правила:
- Всегда отвечай на языке исходного запроса.
- Используй только Markdown-разметку: заголовки (#, ##, ###), абзацы и списки (- item).
- Никаких других тегов, ссылок или HTML в тексте.
- Статья должна быть связной, аргументированной и раскрывать тему в соответствии \
  с векторами исследования.
- Опирайся на содержание предоставленных страниц, синтезируй информацию и выявляй \
  противоречия, закономерности, ключевые выводы.
- Не пересказывай источники дословно — анализируй и делай выводы.\
"""

WRITE_ARTICLE_USER = """\
## Исходный запрос
{query}

## Векторы исследования
{direction}

## Саммари источников (JSON)
{summaries_json}

## Задача
Напиши развёрнутую исследовательскую статью, которая:
1. Раскрывает тему через призму всех векторов исследования.
2. Синтезирует информацию из предоставленных саммари источников.
3. Выявляет противоречия, ключевые идеи и выводы.

Используй структуру с заголовками (#, ##, ###), абзацами и маркированными списками (- item).
"""


def build_write_article_messages(
    query: str,
    direction: str,
    summaries: list[dict],
) -> list[dict]:
    """Формирует список сообщений для написания итоговой исследовательской статьи.

    Args:
        query: Первичный исследовательский запрос пользователя.
        direction: Результат шага direction_brainstorm (векторы исследования).
        summaries: Список словарей с ключами 'url' и 'summary'.

    Returns:
        Список сообщений в формате OpenAI Chat для передачи в LLMClient.generate().
    """
    import json as _json

    summaries_json = _json.dumps(summaries, ensure_ascii=False, indent=2)
    user_content = WRITE_ARTICLE_USER.format(
        query=query,
        direction=direction,
        summaries_json=summaries_json,
    )
    return [
        {"role": "system", "content": WRITE_ARTICLE_SYSTEM},
        {"role": "user", "content": user_content},
    ]


def build_relevance_messages(
    query: str,
    direction: str,
    keyword: str,
    results: list[SearchResultToScore],
) -> list[dict]:
    """Формирует список сообщений для оценки релевантности результатов поиска.

    Args:
        query: Первичный исследовательский запрос пользователя.
        direction: Результат шага direction_brainstorm (векторы исследования).
        keyword: Поисковый запрос, по которому получены результаты.
        results: Список результатов для оценки.

    Returns:
        Список сообщений в формате OpenAI Chat для передачи в LLMClient.generate().
    """
    import json as _json

    results_json = _json.dumps(
        [{"title": r.title, "description": r.description} for r in results],
        ensure_ascii=False,
        indent=2,
    )
    user_content = RELEVANCE_USER.format(
        query=query,
        direction=direction,
        keyword=keyword,
        results_json=results_json,
    )
    return [
        {"role": "system", "content": RELEVANCE_SYSTEM},
        {"role": "user", "content": user_content},
    ]
