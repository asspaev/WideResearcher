"""Промпты для LLM-шагов исследовательского пайплайна."""

DIRECTION_SYSTEM = """\
Ты — экспертный аналитик и методолог исследований. Твоя задача — развивать и углублять \
исследовательскую мысль: находить смежные направления, выявлять неочевидные связи, \
формулировать уточняющие гипотезы и раскрывать скрытые аспекты темы.

Правила:
- Всегда отвечай на языке исходного запроса.
- Не повторяй мысли, которые уже есть в накопленном контексте.
- Каждое новое направление должно быть конкретным и операционализируемым — \
  его можно проверить через поиск или источники.
- Избегай банальных обобщений («изучить тему подробнее»).
- Структурируй ответ как пронумерованный список новых векторов исследования.\
"""

DIRECTION_USER = """\
## Первичный запрос исследования
{query}

## Накопленные мысли и направления (предыдущие итерации)
{accumulated_context}

## Задача
Предложи {n_vectors} новых векторов для углубления этого исследования.
Каждый вектор — это конкретная подтема, гипотеза или угол зрения, \
который ещё не раскрыт в накопленном контексте.

Формат каждого пункта:
[Номер]. [Название вектора] — [1–2 предложения: суть направления и почему оно важно для \
понимания исходного запроса]
"""


def build_direction_messages(
    query: str,
    accumulated_context: str,
    n_vectors: int = 5,
) -> list[dict]:
    """Формирует список сообщений для шага брейншторма направления исследования.

    Args:
        query: Первичный исследовательский запрос пользователя.
        accumulated_context: Текст с уже накопленными мыслями и направлениями
            из предыдущих итераций. Пустая строка, если это первая итерация.
        n_vectors: Количество новых векторов, которые должна предложить модель.

    Returns:
        Список сообщений в формате OpenAI Chat для передачи в LLMClient.generate().
    """
    user_content = DIRECTION_USER.format(
        query=query,
        accumulated_context=accumulated_context or "Контекст пока отсутствует — это первая итерация.",
        n_vectors=n_vectors,
    )
    return [
        {"role": "system", "content": DIRECTION_SYSTEM},
        {"role": "user", "content": user_content},
    ]
